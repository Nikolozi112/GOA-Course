# 1) გაიხსენეთ და ახსენით წესები რომლებიც მუშაობს ყველა ლოგიკურ ოპერატორზე რაც დღეს ვისწავლეთ (and, or, not)

# თუ ვიყენებთ ლოგიკურ ოპერატორ and-ს მხოლოდ მაშინ გამოდის ტერმინალში True თუ ორივე კომპონენტი არის True.
# თუ ვიყენებთ ლოგიკურ ოპერატორ or-ს მხოლოდ მაშინ გამოდის ტერმინალში False თუ ორივე კომპონენტი არის False.
# როცა ვიყენებთ not ოპერატორს not True-ს ტერმინალში გამოაქვს False ხოლო not False-ს მაშინ გამოაქვს True.


# 2)  შექმენით and ოპერატორის მეშვეობით 5 სხვადასხვა მაგალითი

print(True and True)
print(True and False)
print(False and True)
print(False and False)
print(True and False and True)

# 3) შექმენით or ოპერატორის მეშვეობით 5 სხვადასხვა მაგალითი

print(True or True)
print(True or False)
print(False or True)
print(False or False)
print(True or False or True)

# 4) შეურიეთ შედარების და ლოგიკური ოპერატორები და გააკეთეთ 5 მაგალითი

