# 1) განიხილეთ ყველა შედარების ოპერატორი რომელიც დღეს ვისწავლეთ, ახსენით როგორ მუშაობს და თითოეულზე რა შემთხვევაში გამოაქვს True და რა შემთხვევაში გამოაქვს False

# != - არ უდრის 
# == - უდრის
# < - მეტია 
# > - ნაკლებია
# <= - მეტია ან ტოლია
# >= - ნაკლებია ან ტოლია

num1 = 8
num2 = 4

# ორ ინტეჯერს ადარებს ერთმანეთს და თუ ჭეშმარია ტერმინალში გამოაქვს True თუ არა False.

print(num1 != num2) 
print(num1 == num2) 
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)